package net.lax1dude.eaglercraft.backend.server.api;

public class TLSManagerException extends Exception {

	private static final long serialVersionUID = -8110945929693568131L;

	public TLSManagerException() {
	}

	public TLSManagerException(String message, Throwable cause) {
		super(message, cause);
	}

	public TLSManagerException(String message) {
		super(message);
	}

	public TLSManagerException(Throwable cause) {
		super(cause);
	}

}
